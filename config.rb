# Methods defined in the helpers block are available in templates
helpers do
  def most_recent(articles, tag, number=nil)
    number = number || articles.length + 1;
    blog = articles.select do |article|
      article.tags.include?(tag)
    end
    blog[0..(number-1)]
  end
end

